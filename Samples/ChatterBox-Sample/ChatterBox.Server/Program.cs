using System;

namespace ChatterBox.Server
{
    internal class Program
    {
        private static void Main()
        {
            try
            {
                var signallingServer = new ChatterBoxServer();
                signallingServer.Run();
                Console.ReadLine();
            }
            catch (Exception ex)
            {
                Console.WriteLine(ex.Message);
                Console.ReadLine();
            }
        }
    }
}